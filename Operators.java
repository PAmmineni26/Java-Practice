public class Operators {
    public static void main(String args[])
    {
        System.out.println("5+6 is: "+(5+6));
        System.out.println("5-6 is: "+(5-6));
        System.out.println("5/6 is: "+(5/6));
        System.out.println("5%6 is: "+(5%6));
        System.out.println("5*6 is: "+(5*6));

        int a=(int)15.56;
        int ch=65;
        System.out.println("A Value is: "+a);
        System.out.println("A Value is: "+ch);
    }
}