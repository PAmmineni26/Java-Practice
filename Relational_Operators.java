public class Relational_Operators {
    public static void main(String args[])
    {
        int a=12,b=22;
        System.out.println("a<b is: "+(a<b));
        System.out.println("a>b is: "+(a>b));
        System.out.println("a<=b is: "+(a<=b));
        System.out.println("a>=b is: "+(a>=b));
        System.out.println("a==b is: "+(a==b)); // == is required '=' doesn't not work
        System.out.println("a!=b is: "+(a!=b)); // '!=' = not equal to
    }
}